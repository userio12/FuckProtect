/**
 * Encrypted string storage and runtime decryption for FuckProtect shell.
 *
 * All sensitive strings (class names, file paths, log tags, error messages)
 * are stored as XOR-encrypted byte arrays and decrypted only at point of use,
 * then immediately wiped from memory.
 *
 * Build-time encryption: A Python/Kotlin script runs before native compilation
 * to encrypt all strings listed in STRING_TABLE and emit the encrypted bytes.
 * For now, strings are manually encrypted below.
 *
 * T8.4 + T8.5: Full implementation with all sensitive strings.
 */

#include <stdint.h>
#include <string.h>
#include <stdlib.h>

/* ─── XOR key (4 bytes, rotating) ─────────────────────────────────── */
/* Should be changed per build in production */
static const uint8_t STR_XOR_KEY[4] = { 0xA7, 0x3C, 0xE1, 0x5D };

/* ─── Decryption function ──────────────────────────────────────────── */

/**
 * Decrypt a string in-place into the output buffer.
 * Caller must wipe the output buffer after use.
 *
 * @param encrypted  Encrypted byte array
 * @param len        Length of encrypted data
 * @param out        Output buffer (must be >= len + 1)
 * @return Pointer to out
 */
static const char *str_decrypt(const uint8_t *encrypted, int len, char *out) {
    for (int i = 0; i < len; i++) {
        out[i] = (char)(encrypted[i] ^ STR_XOR_KEY[i % 4]);
    }
    out[len] = '\0';
    return out;
}

