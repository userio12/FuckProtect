/* ─── Public API ───────────────────────────────────────────────────── */

/**
 * Decrypt string by ID into caller's buffer.
 *
 * Usage:
 *   char buf[64];
 *   str_get(STR_FP_SHELL_APP, buf, sizeof(buf));
 *   // ... use buf ...
 *   memset(buf, 0, sizeof(buf));  // WIPE AFTER USE
 *
 * @param id       String ID from enum
 * @param out      Output buffer
 * @param out_size Size of output buffer
 * @out Pointer to out, or NULL if error
 */
const char *str_get(int id, char *out, int out_size) {
    if (id < 0 || id >= STR__MAX) return NULL;
    if (!out || out_size <= 0) return NULL;

    const str_entry_t *entry = &STRING_TABLE[id];
    if (entry->len >= out_size) return NULL;  /* Buffer too small */

    return str_decrypt(entry->data, entry->len, out);
}

/**
 * Get string length (without decrypting).
 */
int str_len(int id) {
    if (id < 0 || id >= STR__MAX) return 0;
    return STRING_TABLE[id].len;
}
